export default function AdminSidebar({
  user,
}: {
  user: { name?: string | null; email?: string | null; role: string };
}) {
  const pathname = usePathname();
  const [mobileOpen, setMobileOpen] = useState(false);

  const isActive = (href: string) =>
    href === "/admin" ? pathname === "/admin" : pathname.startsWith(href);

  return (
    <>
      {/* Mobile hamburger */}
      <button
        onClick={() => setMobileOpen(true)}
        className="md:hidden fixed top-4 left-4 z-50 bg-[#25497f] text-white p-2 rounded-lg shadow-lg"
      >
        <MenuIcon />
      </button>

      {/* Overlay */}
      {mobileOpen && (
        <div
          className="md:hidden fixed inset-0 bg-black/50 z-40"
          onClick={() => setMobileOpen(false)}
        />
      )}

      {/* Sidebar */}
      <aside
        className={`fixed top-0 left-0 h-full w-56 bg-[#25497f] text-white z-50 flex flex-col transition-transform duration-200
          ${mobileOpen ? "translate-x-0" : "-translate-x-full md:translate-x-0"}`}
      >
        <div className="p-4 border-b border-white/20">
          <Link href="/admin" className="text-lg font-bold block leading-tight">
            Pir Reklam
          </Link>
          <span className="text-xs text-white/50">Yönetim Paneli</span>
        </div>

        <nav className="flex-1 py-2 overflow-y-auto">
          {navItems.map((item) => (
            <Link
              key={item.href}
              href={item.href}
              onClick={() => setMobileOpen(false)}
              className={`flex items-center gap-3 px-4 py-2.5 text-sm transition-colors hover:bg-white/10
                ${isActive(item.href) ? "bg-white/20 font-semibold border-r-2 border-white" : ""}`}
            >
              <item.icon />
              <span>{item.label}</span>
            </Link>
          ))}
        </nav>

        <div className="p-4 border-t border-white/20 space-y-3">
          <a
            href="/"
            target="_blank"
            rel="noopener noreferrer"
            className="w-full flex items-center justify-center gap-2 px-3 py-2 rounded-lg bg-white/10 text-white text-sm font-medium hover:bg-white/20 transition-colors"
          >
            <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M10 6H6a2 2 0 00-2 2v10a2 2 0 002 2h10a2 2 0 002-2v-4M14 4h6m0 0v6m0-6L10 14" />
            </svg>
            Siteyi Gör
          </a>
          <div>
            <p className="text-sm font-medium truncate">{user.name || user.email}</p>
            <p className="text-xs text-white/60 uppercase tracking-wide">{user.role}</p>
          </div>
          <button
            onClick={async () => {
              await fetch("/api/auth/logout", { method: "POST" });
              window.location.href = "/admin/giris";
            }}
            className="w-full text-left text-sm text-white/80 hover:text-white flex items-center gap-2"
          >
            <LogoutIcon />
            <span>Çıkış Yap</span>
          </button>
        </div>
      </aside>
    </>
  );
}

